package entity;

/**
 *
 * @author binh2
 */
public class Product {
    String id,name,describe,img,price,quantity;

    public Product(String id, String name, String describe, String img, String price, String quantity) {
        this.id = id;
        this.name = name;
        this.describe = describe;
        this.img = img;
        this.price = price;
        this.quantity = quantity;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescribe() {
        return describe;
    }

    public void setDescribe(String describe) {
        this.describe = describe;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    @Override
    public String toString() {
        return "Product{" + "id=" + id + ", name=" + name + ", describe=" + describe + ", img=" + img + ", price=" + price + ", quantity=" + quantity + '}';
    }
    
    
}
